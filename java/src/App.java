import java.util.*;

public class App {

    private static void print(int[][] A) {
        for (int i = 0; i < A.length; i++) {
            for (int j = 0; j < A[i].length; j++) {
                System.out.print(A[i][j]+ " ");
            }
            System.out.println();

        }
    }
        
    
    public static void main(String[] args) throws Exception {

        int[][] A = {
            {1,2,3},
            {4,5,6},
            {7,8,9}
        };
    }

    
}
